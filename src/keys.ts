export function getKeyName(key: number) {
  return key < 0
    ? "UNBOUND"
    : key === 20000
    ? "SCROLL"
    : key > 10000
    ? "M" + (key - 10000)
    : keyboardMap[key];
}

export function getKeyCode(event: MouseEvent | KeyboardEvent) {
  if (event instanceof MouseEvent) return 10000 + event.which;
  else return event.keyCode;
}
